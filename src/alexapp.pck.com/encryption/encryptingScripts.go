package encryption

import (
	"crypto/sha512"
	"encoding/hex"
)

//CreateHash creates a hash based on a key
func CreateHash(key string) string {
	hasher := sha512.New()
	hasher.Write([]byte(key))
	return hex.EncodeToString(hasher.Sum(nil))
}

